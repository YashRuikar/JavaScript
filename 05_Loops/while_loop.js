// while loop

let index = 0
while (index <= 10) {
    console.log(`Value of index is ${index}`);
    index = index + 2
}


let myArray = ["Flash", "Batman", "Superman"]

let a = 0
while (a < myArray.length) {
    console.log(`Value is ${myArray[a]}`);
    a++
}


// do while loop 

let score = 1

do {
    console.log(`Score is ${score}`);
    score++
} while (score <= 10);